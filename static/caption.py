    urls = re.findall(url_pattern, link)
    print(urls)
    caption = f'<a href="{urls[0]}"><b>{link_text}</b></a>\n\n' \
              f'<b>–ù–∞—á–∞–ª—å–Ω–∞—è —Ü–µ–Ω–∞: </b>{start_price} —Ä—É–±.\n' \
              f'<b>–ú–∏–Ω–∏–º–∞–ª—å–Ω–∞—è —Ü–µ–Ω–∞: </b>{min_price} —Ä—É–±.\n' \
              f'<b>–¢–µ–∫—É—â–∞—è —Ü–µ–Ω–∞: </b>{price} —Ä—É–±.'
    return caption


